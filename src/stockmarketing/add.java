    public void table_update(){
        
        int c;
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/first","root","");
            pst = con.prepareStatement("select * from add1");
            ResultSet Rs=pst.executeQuery();
            
            java.sql.ResultSetMetaData rd = Rs.getMetaData();
            c = rd.getColumnCount();
            DefaultTableModel dl = (DefaultTableModel)jTable1.getModel();
            dl.setRowCount(0);
            
            while(Rs.next())
            {
                Vector v2 = new Vector();
                
                for(int i=1;i<=c;i++)
                {
                    v2.add(Rs.getString("ID"));
                    v2.add(Rs.getString("model"));
                    v2.add(Rs.getString("available"));
                    
                }
                dl.addRow(v2);
            }
            
            
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(add.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(add.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }
