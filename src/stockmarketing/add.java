    public void autoID(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/first","root","");
            Statement s= con.createStatement();
            ResultSet rs = s.executeQuery("select MAX(ID) from add1");
            rs.next();
            rs.getString("MAX(ID)");
            
            if(rs.getString("MAX(ID)")==null){
                addid.setText("S0001");
            }
            else{
                long id = Long.parseLong(rs.getString("MAX(ID)").substring(1,rs.getString("MAX(ID)").length()));
                id++;
                
                addid.setText("S0" + String.format("%03d",id));
                
            }


        } catch (ClassNotFoundException ex) {
            Logger.getLogger(add.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(add.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
            
    
    
    
